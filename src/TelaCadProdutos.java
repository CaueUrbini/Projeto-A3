    private void consultar() {
        String sql = "select * from tbl_produto where id_produto=?";
        try {
            pst = conexao.prepareStatement(sql);
            pst.setString(1, txtId.getText());
            rs = pst.executeQuery();
            if (rs.next()) {
                txtNome.setText(rs.getString(2));
                txtCod.setText(rs.getString(3));
                txtPreco.setText(rs.getString(4));
                txtQuant.setText(rs.getString(5));

            } else {

                JOptionPane.showMessageDialog(null, "produto não cadastrado");
                txtNome.setText(null);
                txtCod.setText(null);
                txtPreco.setText(null);
                txtQuant.setText(null);

            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }

    }

    private void adicionar() {
        String sql = "insert into tbl_produto(id_produto, nome, cod_produto, valor, quantidade ) values(?,?,?,?,?)";
        try {
            pst = conexao.prepareCall(sql);
            pst.setString(1, null);
            pst.setString(2, txtNome.getText());
            pst.setString(3, txtCod.getText());
            pst.setString(4, txtPreco.getText());
            pst.setString(5, txtQuant.getText());
            if ((txtNome.getText().isEmpty()) || (txtCod.getText().isEmpty())) {
                JOptionPane.showMessageDialog(null, "Preencha todos os campos obrigatórios");

            } else {

                int adicionado = pst.executeUpdate();
                if (adicionado > 0) {
                    JOptionPane.showMessageDialog(null, "produto cadastrado");
                    txtNome.setText(null);
                    txtCod.setText(null);
                    txtPreco.setText(null);
                    txtQuant.setText(null);
                }
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }

    }

    private void alterar() {
        String sql = "update tbl_produto set nome=?, cod_produto=?, valor=?, quantidade=? where id_produto=?";
        try {
            pst = conexao.prepareStatement(sql);
            pst.setString(1, txtNome.getText());
            pst.setString(2, txtCod.getText());
            pst.setString(3, txtPreco.getText());
            pst.setString(4, txtQuant.getText());
            pst.setString(5, txtId.getText());
            if ((txtNome.getText().isEmpty()) || (txtCod.getText().isEmpty())) {
                JOptionPane.showMessageDialog(null, "Preencha todos os campos obrigatórios");

            } else {

                int adicionado = pst.executeUpdate();
                if (adicionado > 0) {
                    JOptionPane.showMessageDialog(null, "alteração realizada com sucesso");
                    txtNome.setText(null);
                    txtCod.setText(null);
                    txtPreco.setText(null);
                    txtQuant.setText(null);
                }
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }

    }

